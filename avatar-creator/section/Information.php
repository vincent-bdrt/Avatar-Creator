          <h2>Informations</h2>
          <h3>Présentations</h3>
          <p>Le site est en cours de développement, il est donc possible que certaines fonctionnalités ne soient pas
              encore disponibles.</p>
          <p>
              PinkNose.me est un créateur d'Avatar où les possibilités sont infinies. Dans ce projet il est possible de
              créer un avatar de toutes pièces en choisissant la couleur de cheveux, les vêtements et également des
              costumes. Une fois que votre avatar est prêt vous pouvez le télécharger. Il est également possible de
              changer sa morphology et de choisir aléatoirement sa tenue et son apparence. <br><br>

              Je développe ce projet sur mon temps libre depuis environ 1 an. Le langage PHP est également utilisé afin
              d'automatiser certaines actions comme par
              exemple faciliter l’ajout de nouveau contenu en ciblant le répertoire désiré.<br>
              Plusieurs versions sont sorties depuis le début du projet et à chaque fois l'optimisation afin de
              permettre
              une lecture simple du code a été effectuée.<br><br>

              Une documentation sera bientôt fournie pour permettre à tous de pouvoir créer son propre jeu. J’envisage
              par
              la suite de créer un Back-Office afin de permettre à des personnes vraiment novice en programmation de
              créer
              leur propre jeu.
          </p>
